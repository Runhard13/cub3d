#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ptr;

	if (!s)
		return (NULL);
	if (ft_strlen(s) < start)
		return (ft_strdup(""));
	if (!(ptr = malloc(len + 1)))
		return (NULL);
	s += start;
	ft_memcpy(ptr, s, len);
	ptr[len] = '\0';
	return (ptr);
}
