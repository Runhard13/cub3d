#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	len;

	i = 0;
	if (!s1 || !set)
		return (NULL);
	while (s1[i] && ft_strchr(set, s1[i]))
		i++;
	len = ft_strlen(&s1[i]);
	while (len && ft_strchr(set, s1[(i + (len - 1))]))
		len--;
	return (ft_substr(s1, i, len));
}
