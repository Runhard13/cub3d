#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	size_t		i;
	char		*d;
	const char	*s;

	d = dest;
	s = src;
	i = 0;
	while (i < n)
	{
		if ((*d++ = *s++) == (char)c)
			return (d);
		i++;
	}
	return (NULL);
}
